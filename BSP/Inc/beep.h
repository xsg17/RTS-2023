#define PORT_BEEP    GPIOC
#define PIN_BEEP     GPIO_Pin_5

#define BEEP_PORT_ENABLE()    { RCC_AHB1PeriphClockCmd( RCC_AHB1Periph_GPIOC, ENABLE ); }

#define BEEP_Toggle()    GPIO_ToggleBits( PORT_BEEP, PIN_BEEP )
#define BEEP_ON()        GPIO_SetBits ( PORT_BEEP, PIN_BEEP )
#define BEEP_OFF()       GPIO_ResetBits( PORT_BEEP, PIN_BEEP )
#define BEEP_SIGNAL()    { BEEP_ON(); delay_ms(1000); BEEP_OFF(); delay_ms(50); }
#define BEEP_ALARM()     { BEEP_ON(); delay_ms(200); BEEP_OFF(); delay_ms(50); BEEP_ON(); delay_ms(50); BEEP_OFF(); delay_ms(200); }
